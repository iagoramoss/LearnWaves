import Foundation
import SwiftUI

extension Font {
    static func arcon(size: CGFloat) -> Font {
        .custom("Arcon", size: size)
    }
    
    static func antipasto(size: CGFloat) -> Font {
        .custom("Antipasto Pro", size: size)
    }
}
